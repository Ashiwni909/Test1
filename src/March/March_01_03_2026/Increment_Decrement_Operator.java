package March.March_01_03_2026;

public class Increment_Decrement_Operator
{
    static void main()
    {
        int a=10;
        System.out.println( a++ + a++);
        //++a=11
        //a++=10
        //a++11

        int b=10;

        System.out.println(--b + b++ + b--);
        //--b=9
        //b++=9
        //b--=10



    }



}
